def build_session_cmd(name: str, agents: dict) -> tuple[list[str], str]:
    """Build the command and working directory for a session.

    If `name` matches an agent in agents.json, launches Claude Code with
    the agent's system prompt and MCP config. Otherwise, launches a plain shell.

    Returns:
        (cmd, cwd) — command list and working directory path.
    """
    cfg = agents.get(name)
    if cfg:
        cmd = ["claude"]
        if cfg.get("prompt"):
            cmd.extend(["--system-prompt", cfg["prompt"]])
        if cfg.get("mcp_config"):
            mcp_path = cfg["mcp_config"]
            if os.path.exists(mcp_path):
                cmd.extend(["--mcp-config", mcp_path])
        cwd = cfg.get("cwd", os.path.expanduser("~"))
    else:
        # No agent match — plain shell session
        default_shell = "/bin/zsh" if platform.system() == "Darwin" else "/bin/bash"
        cmd = [os.environ.get("SHELL", default_shell)]
        cwd = os.path.expanduser("~")
    return cmd, cwd


# xterm.js sends DA1 queries (\x1b[c) on init. The terminal responds with
# \x1b[?1;2c etc., which can leak through as visible ghost text in the prompt.
# We strip these from PTY output before sending to the client.
_DA_RESPONSE_RE = re.compile(rb'\x1b\[\?[0-9;]*c')

def _blocking_read(fd: int) -> bytes:
    """Read from PTY fd with batching — collect available data up to 64KB."""
    if fd < 0:
        return b""
    try:
        r, _, _ = select.select([fd], [], [], 0.05)
        if not r:
            return b""
        chunks = []
        total = 0
        while total < 65536:
            try:
                chunk = os.read(fd, 65536 - total)
                if not chunk:
                    break
                chunks.append(chunk)
                total += len(chunk)
                r, _, _ = select.select([fd], [], [], 0)
                if not r:
                    break
            except (OSError, BlockingIOError):
                break
        data = b"".join(chunks)
        # Strip Device Attribute responses that leak as visible text
        data = _DA_RESPONSE_RE.sub(b'', data)
        return data
    except (OSError, ValueError):
        pass
    return b""



async def _reap_loop():
    """Periodically reap dead sessions."""
    while True:
        pool.list_names()
        await asyncio.sleep(15)


