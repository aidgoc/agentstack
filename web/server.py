def mirror_to_wave(session_name: str) -> bool:
    """Mirror a tmux session to a Wave terminal block if Wave is running.

    Returns True if Wave was found and wsh was invoked, False otherwise.
    Silently skips if Wave is not running or wsh is not available.
    """
    if not WSH_BIN:
        log.debug("wsh not found — skipping Wave mirror")
        return False
    try:
        result = subprocess.run(
            ["pgrep", "wavesrv"],
            capture_output=True, timeout=3
        )
        if result.returncode != 0:
            log.debug("Wave not running — skipping mirror")
            return False
    except Exception:
        return False

    sess = tmux_session_name(session_name)
    try:
        subprocess.Popen(
            [WSH_BIN, "run", "--", "tmux", "attach", "-t", sess],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
        )
        log.info("Mirrored session '%s' to Wave", sess)
        return True
    except Exception as e:
        log.warning("Wave mirror failed for '%s': %s", sess, e)
        return False


def tmux_session_exists(name: str) -> bool:
    """Check if a tmux session exists."""
    sess = tmux_session_name(name)
    try:
        result = subprocess.run(
            ["tmux", "has-session", "-t", sess],
            capture_output=True, timeout=5
        )
        return result.returncode == 0
    except Exception:
        return False


# ── PTY Session (attaches to tmux or runs raw) ──────────

class PtySession:
    def __init__(self, name: str, cmd: list[str], cwd: str = None, env_extra: dict = None, use_tmux: bool = True):
        self.name = name
        self.pid = -1
        self.fd = -1
        self._alive = False
        self.replay_buffer = bytearray()
        self.uses_tmux = use_tmux and HAS_TMUX
        self.cwd = cwd or os.path.expanduser("~")

        os.makedirs(self.cwd, exist_ok=True)

        env = os.environ.copy()
        env["TERM"] = "xterm-256color"
        env["COLORTERM"] = "truecolor"
        for var in _STRIP_ENV:
            env.pop(var, None)
        if env_extra:
            env.update(env_extra)

        if self.uses_tmux:
            # Create tmux session if it doesn't exist
            if not tmux_session_exists(name):
                tmux_create_session(name, cmd, self.cwd, env_extra)
                time.sleep(0.3)
                # Wait for tmux session to be ready before mirroring
                ready = False
                for _ in range(6):
                    if tmux_session_exists(name):
                        ready = True
                        break
                    time.sleep(0.2)
                if ready:
                    mirror_to_wave(name)
            else:
                # Session already exists — mirror to Wave on re-attach too
                mirror_to_wave(name)

            # Attach to tmux session via PTY
            attach_cmd = ["tmux", "attach", "-t", tmux_session_name(name)]
            self.pid, self.fd = pty.fork()
            if self.pid == 0:
                os.chdir(self.cwd)
                os.execvpe(attach_cmd[0], attach_cmd, env)
            else:
                self._alive = True
                flags = fcntl.fcntl(self.fd, fcntl.F_GETFL)
                fcntl.fcntl(self.fd, fcntl.F_SETFL, flags | os.O_NONBLOCK)
                log.info("tmux PTY '%s' started (pid=%d)", name, self.pid)
        else:
            # Raw PTY fallback
            self.pid, self.fd = pty.fork()
            if self.pid == 0:
                os.chdir(self.cwd)
                os.execvpe(cmd[0], cmd, env)
            else:
                self._alive = True
                flags = fcntl.fcntl(self.fd, fcntl.F_GETFL)
                fcntl.fcntl(self.fd, fcntl.F_SETFL, flags | os.O_NONBLOCK)
                log.info("PTY '%s' started (pid=%d)", name, self.pid)

    def append_replay(self, data: bytes):
        self.replay_buffer.extend(data)
        if len(self.replay_buffer) > REPLAY_BUFFER_SIZE:
            self.replay_buffer = self.replay_buffer[-REPLAY_BUFFER_SIZE:]

    def get_replay(self) -> str:
        return self.replay_buffer.decode("utf-8", errors="replace")

    def resize(self, cols: int, rows: int):
        if self.fd >= 0:
            try:
                fcntl.ioctl(self.fd, termios.TIOCSWINSZ, struct.pack("HHHH", rows, cols, 0, 0))
            except OSError:
                pass

    def write(self, data: bytes):
        if self.fd >= 0 and self._alive:
            try:
                os.write(self.fd, data)
            except OSError:
                self._alive = False

    def is_alive(self) -> bool:
        if not self._alive:
            return False
        try:
            pid, _ = os.waitpid(self.pid, os.WNOHANG)
            if pid != 0:
                self._alive = False
        except ChildProcessError:
            self._alive = False
        # For tmux sessions, also check if the tmux session itself is alive
        if not self._alive and self.uses_tmux:
            return tmux_session_exists(self.name)
        return self._alive

    def kill(self):
        self._alive = False
        if self.fd >= 0:
            try:
                os.close(self.fd)
            except OSError:
                pass
            self.fd = -1
        if self.pid > 0:
            try:
                os.kill(self.pid, signal.SIGTERM)
                for _ in range(30):
                    pid, _ = os.waitpid(self.pid, os.WNOHANG)
                    if pid != 0:
                        break
                    time.sleep(0.1)
                else:
                    try:
                        os.kill(self.pid, signal.SIGKILL)
                        os.waitpid(self.pid, 0)
                    except (OSError, ChildProcessError):
                        pass
            except (OSError, ChildProcessError):
                pass
        # Also kill the tmux session
        if self.uses_tmux:
            tmux_kill_session(self.name)
        log.info("PTY '%s' killed", self.name)


class SessionPool:
    def __init__(self):
        self.sessions: dict[str, PtySession] = {}

    def spawn(self, key: str, cmd: list[str], cwd: str = None, env_extra: dict = None) -> PtySession:
        if key in self.sessions:
            old = self.sessions[key]
            # Just detach the old PTY, don't kill tmux session if re-attaching
            old._alive = False
            if old.fd >= 0:
                try:
                    os.close(old.fd)
                except OSError:
                    pass
                old.fd = -1
            if old.pid > 0:
                try:
                    os.kill(old.pid, signal.SIGTERM)
                except (OSError, ChildProcessError):
                    pass
        try:
            session = PtySession(key, cmd, cwd, env_extra)
        except Exception:
            self.sessions.pop(key, None)
            raise
        self.sessions[key] = session
        return session

    def get(self, key: str) -> PtySession | None:
        s = self.sessions.get(key)
        if s and not s.is_alive():
            del self.sessions[key]
            return None
        return s

    def kill(self, key: str) -> bool:
        s = self.sessions.pop(key, None)
        if s:
            s.kill()
            return True
        return False

    def list_names(self) -> list[str]:
        # Include tmux sessions that aren't yet attached
        attached = set()
        dead = []
        for k, s in self.sessions.items():
            if not s.is_alive():
                dead.append(k)
            else:
                attached.add(k)
        for k in dead:
            del self.sessions[k]

        # Discover tmux sessions not yet in our pool
        if HAS_TMUX:
            for name in tmux_list_sessions():
                if name not in attached:
                    attached.add(name)

        return sorted(attached)

    def kill_all(self):
        for s in self.sessions.values():
            s.kill()
        self.sessions.clear()


