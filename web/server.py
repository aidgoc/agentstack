"""
AgentStack Terminal Server
==========================

WebSocket-based terminal server that bridges xterm.js ↔ PTY sessions.

Architecture:
  Telegram Mini App (xterm.js) → WebSocket → FastAPI → PTY (via tmux)

Key features:
  - tmux backend: sessions persist across server restarts
  - 64KB replay buffer: reconnecting clients get terminal state back
  - ping/pong keepalive (20s): detects dead mobile connections
  - Per-agent MCP configs: each agent launches with specific tools
  - File upload/download: REST endpoints for drag-and-drop transfer
  - Device Attribute filtering: strips xterm.js DA responses that leak as ghost text

Auth:
  Single-owner model. First WebSocket message must be a valid HMAC token
  generated by the Telegram bot (see users.py).

Usage:
  python3 web/server.py                    # direct
  uvicorn web.server:app --host 0.0.0.0    # via uvicorn
"""

